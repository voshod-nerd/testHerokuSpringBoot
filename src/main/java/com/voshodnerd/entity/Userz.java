package com.voshodnerd.entity;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Талалаев
 */
@Entity
@Table(name = "userz")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Userz.findAll", query = "SELECT u FROM Userz u"),
    @NamedQuery(name = "Userz.findById", query = "SELECT u FROM Userz u WHERE u.id = :id"),
    @NamedQuery(name = "Userz.findByUsername", query = "SELECT u FROM Userz u WHERE u.username = :username"),
    @NamedQuery(name = "Userz.findByRoles", query = "SELECT u FROM Userz u WHERE u.roles = :roles")})
public class Userz implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 250)
    @Column(name = "username")
    private String username;
    @Size(max = 50)
    @Column(name = "roles")
    private String roles;
    @OneToMany(mappedBy = "fromuser")
    private Collection<Messages> messagesCollection;

    public Userz() {
    }

    public Userz(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getRoles() {
        return roles;
    }

    public void setRoles(String roles) {
        this.roles = roles;
    }

    @XmlTransient
    public Collection<Messages> getMessagesCollection() {
        return messagesCollection;
    }

    public void setMessagesCollection(Collection<Messages> messagesCollection) {
        this.messagesCollection = messagesCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Userz)) {
            return false;
        }
        Userz other = (Userz) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.voshodnerd.entity.Userz[ id=" + id + " ]";
    }
    
}
